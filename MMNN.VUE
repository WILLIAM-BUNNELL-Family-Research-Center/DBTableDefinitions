/* MEMBER NOTES JOINED TABLE */

DROP VIEW BUNNELL.MMNN;

CREATE VIEW BUNNELL.MMNN
       (MEMBF,
        MFN,
        MMN,
        MLN,
        MDBY,
        MDBM,
        MPB,
        MBTN,
        MDDY,
        MDDM,
        MPD,
        MDTN,
        MG,
        MBY,
        MBM,
        MCB,
        PF,
        PSP,
        GEN,
        ND,
        NL,
        NP,
        NTN,
        NTXT,
        NRF,
        NSRF)
AS     (SELECT
        MEMBER.MEMBF,
        MEMBER.MFN,
        MEMBER.MMN,
        MEMBER.MLN,
        MEMBER.MDBY,
        MEMBER.MDBM,
        MEMBER.MPB,
        MEMBER.MBTN,
        MEMBER.MDDY,
        MEMBER.MDDM,
        MEMBER.MPD,
        MEMBER.MDTN,
        MEMBER.MG,
        MEMBER.MBY,
        MEMBER.MBM,
        MEMBER.MCB,
        MEMBER.PF,
        MEMBER.PSP,
        MEMBER.GEN,
         NOTES.ND,
         NOTES.NL,
         NOTES.NP,
         NOTES.NTN,
         NOTES.NTXT,
         NOTES.NRF,
         NOTES.NSRF
    FROM BUNNELL.MM MEMBER LEFT OUTER JOIN BUNNELL.NN NOTES
             ON MEMBER.MEMBF = NOTES.MEMBF);